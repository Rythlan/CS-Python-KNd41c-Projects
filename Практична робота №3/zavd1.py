def main():
    text = input("Введіть, будь ласка, рядок: ")

    while len(text) < 2:
        text = input("Рядок занадто короткий для виконання операції, спробуйте ще раз: ")

    result = text[:-1][::-1]
    print(f"Результат отриманого зрізу у зворотному порядку: {result}")


if __name__ == "__main__":
    main()